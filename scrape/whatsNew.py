from bs4 import BeautifulSoup
import requests

#returns list of new course id's
def whatsNew():

	new = []
	prefix = "https://registrar.princeton.edu"
	url = prefix + "/course-offerings/whats-new/"

	response = requests.get(url)
	html = response.text
	soup = BeautifulSoup(html, 'html.parser')

	for link in soup.table.find_all('a'):
		id = link.get('href')[46:46+6]
		if id not in new:
			new.append(id)

	return new

if __name__ == '__main__':
	print(whatsNew())


